/* 10 Object Creation
Create an object using an object literal, constructor function, and es6 class.
The object should have the fields name and age.*/

let dog = {
  name: 'Luna',
  age: '2',
  color: 'white',
  breed: 'pomeranian',
  species: 'dog'
};
dog.name;
dog.age;
dog.color;
dog.breed;
dog.species;

console.log(`Hello I'm ${dog.name}.  I am a ${dog.breed} ${dog.species}.  My fur is ${dog.color}.`);
